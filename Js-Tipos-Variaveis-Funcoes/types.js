/* ----------------------------------------------------------------- */
// Type Number 

const primeiroNumero = 10
const segundoNumero = 4

// Com váriaveis do tipo Number podemos fazer operações númericas;

const soma = primeiroNumero + segundoNumero // 14
const divisao = primeiroNumero / segundoNumero // 2.5
const subtracao = primeiroNumero - segundoNumero // 6
const multiplicacao = primeiroNumero * segundoNumero // 40

// Também podemos usar pontos flutuantes;

const numero1 = 5.5 // Lembrando usa-se o (ponto), não a virgula;
const numero2 = .5

// NaN -> Not a Number (não é um número);

const nome = 'Savio'
console.log(nome * primeiroNumero) // NaN

/* ----------------------------------------------------------------- */
// Type String

// Tipos de váriaveis no tipo texto, podemos usar tanto aspas simples ', como duplas ";
const texto1 = 'Meu nome é ';

// Concatenação (+);
console.log(texto1 + nome)

// Template String OU template literal;

/* ----------------------------------------------------------------- */
// Type Boolean

const boolean1 = 12
const boolean2 = 14

console.log(boolean1 === boolean2) // false;

// true -> verdadeiro;
// false -> falso;